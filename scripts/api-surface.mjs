/**
 * Public API surface report — prints every export of the packaged .d.ts
 * rollups so accidental exports are visible. Run after `npm run build:lib`;
 * pass a file path as the first argument to write the report there.
 */
import { readFileSync, writeFileSync } from "node:fs";

const entries = {
  "@modyra/forms": "projects/modyra/dist/types/modyra-forms.d.ts",
  "@modyra/forms/core": "projects/modyra/dist/types/modyra-forms-core.d.ts",
  "@modyra/forms/zod": "projects/modyra/dist/types/modyra-forms-zod.d.ts",
  "@modyra/forms/interop": "projects/modyra/dist/types/modyra-forms-interop.d.ts",
};

let md = `# Public API surface

Generated by \`node scripts/api-surface.mjs\` from the packaged type rollups —
regenerate after every \`npm run build:lib\`. Review this file in code review:
anything appearing here is public API covered by semver.
`;

for (const [entry, file] of Object.entries(entries)) {
  const src = readFileSync(file, "utf8");
  const decls = [...src.matchAll(/^export declare (?:abstract )?(class|function|const|interface|type|enum) ([A-Za-z0-9_]+)/gm)]
    .map((m) => `${m[2]} (${m[1]})`);
  const reexports = [...src.matchAll(/^export \{([^}]+)\}/gms)]
    .flatMap((m) => m[1].split(",").map((s) => s.trim()).filter(Boolean));
  const all = [...new Set([...decls, ...reexports])].sort((a, b) => a.localeCompare(b));
  md += `\n## \`${entry}\` — ${all.length} exports\n\n`;
  for (const name of all) md += `- ${name}\n`;
}

const out = process.argv[2];
if (out) {
  writeFileSync(out, md);
  console.log(`${out} written`);
} else {
  console.log(md);
}
